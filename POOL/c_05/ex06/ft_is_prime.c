int	ft_is_prime(int nb)
{
	long int	i;
	long int	n;

	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	i = 2;
	n = 1;
	while (n * n < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
		n++;
	}
	return (1);
}
