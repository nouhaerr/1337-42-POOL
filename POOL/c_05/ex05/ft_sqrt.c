int	ft_sqrt(int nb)
{
	long long	i;
	long long	sqrt;

	i = 0;
	if (nb < 0)
		return (0);
	while (i * i <= nb)
	{
		sqrt = i * i;
		if (sqrt == nb)
			return (i);
		i++;
	}
	return (0);
}
